CREATE EXTENSION IF NOT EXISTS "uuid-ossp";
CREATE EXTENSION IF NOT EXISTS hstore;

CREATE TABLE LOGGED_ACTIONS (
	LOGGED_ACTIONS_PK bigserial primary key,
	GROUP_FK uuid NOT NULL,
	USER_FK uuid,
    USER_NAME text,
    TABLE_NAME text NOT NULL,
	TABLE_PK uuid NOT NULL,
    CREATED timestamp with time zone NOT NULL DEFAULT now(),
    ACTION TEXT NOT NULL CHECK (ACTION IN ('I','U','U')),
    ORIGINAL_DATA hstore,
    NEW_DATA hstore
) WITH (fillfactor=100);
 
CREATE INDEX IDX_LOGGED_ACTIONS_TABLE_NAME ON lOGGED_ACTIONS(TABLE_NAME);
CREATE INDEX IDX_LOGGED_CREATED ON LOGGED_ACTIONS(CREATED);

CREATE OR REPLACE FUNCTION if_modified_func() RETURNS TRIGGER AS $body$
DECLARE
    audit_row LOGGED_ACTIONS;
    ORIGINAL_DATA hstore;
    NEW_DATA hstore;
	TABLE_PK uuid;
BEGIN
    IF TG_WHEN <> 'AFTER' THEN
        RAISE EXCEPTION 'if_modified_func() may only run as an AFTER trigger';
    END IF;

    IF (TG_OP = 'UPDATE' AND TG_LEVEL = 'ROW') THEN
        original_data = hstore(OLD.*);
        new_data =  (hstore(NEW.*) - original_data);
        IF new_data = hstore('') THEN
            -- All changed fields are ignored. Skip this update.
            RETURN NULL;
        END IF;
    ELSIF (TG_OP = 'DELETE' AND TG_LEVEL = 'ROW') THEN
        original_data = hstore(OLD.*);
    ELSIF (TG_OP = 'INSERT' AND TG_LEVEL = 'ROW') THEN
        original_data = hstore(NEW.*);
    ELSE
        RETURN NULL;
    END IF;

	select value::uuid into TABLE_PK from each(original_data) where key like '%_pk';

    INSERT INTO LOGGED_ACTIONS (GROUP_FK, USER_FK, USER_NAME, TABLE_NAME, TABLE_PK, ACTION, ORIGINAL_DATA, NEW_DATA)
	VALUES (current_setting('inctrak.group_key')::uuid, current_setting('inctrak.user_key')::uuid, current_setting('inctrak.user_name')::text, TG_TABLE_NAME::text, TABLE_PK, substring(TG_OP,1,1), original_data, new_data);
    RETURN NULL;
END;
$body$
LANGUAGE plpgsql;


CREATE OR REPLACE FUNCTION f_updated_column() 
RETURNS TRIGGER AS $$
BEGIN
    NEW.UPDATED = now();
    RETURN NEW; 
END;
$$ language 'plpgsql';

CREATE OR REPLACE FUNCTION f_groupkey_column() 
RETURNS TRIGGER AS $$
BEGIN
    NEW.GROUP_KEY = lower(regexp_replace(NEW.DESCRIPTION, '[^a-zA-Z0-9]+', '', 'g'));
    RETURN NEW; 
END;
$$ language 'plpgsql';

