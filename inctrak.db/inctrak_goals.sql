CREATE TABLE GROUPS(
	GROUP_PK uuid NOT NULL DEFAULT uuid_generate_v4() PRIMARY KEY,
	DESCRIPTION varchar(256) NOT NULL,
	GROUP_KEY varchar(256) NOT NULL,
	CREATED timestamp with time zone NOT NULL DEFAULT now(),
	UPDATED timestamp with time zone NOT NULL DEFAULT now()
);
CREATE UNIQUE INDEX UC_GROUP_DESC ON GROUPS (lower(DESCRIPTION));
CREATE UNIQUE INDEX UC_GROUP_KEY ON GROUPS (GROUP_KEY);
CREATE TRIGGER group_key_groups BEFORE UPDATE ON GROUPS FOR EACH ROW EXECUTE PROCEDURE  f_groupkey_column();
CREATE TRIGGER updated_groups BEFORE UPDATE ON GROUPS FOR EACH ROW EXECUTE PROCEDURE  f_updated_column();

CREATE TABLE USERS(
	USER_PK uuid NOT NULL DEFAULT uuid_generate_v4() PRIMARY KEY,
	GROUP_FK uuid NOT NULL REFERENCES GROUPS,
	USER_NAME varchar(256) NOT NULL,
	EMAIL_ADDRESS varchar(256) NOT NULL,
	ACTIVATED bool NOT NULL,
	PASSWORD varchar(512) NOT NULL,
	ADMINISTRATOR bool NOT NULL,
	ACCEPT_TERMS bool NOT NULL,
	GOOGLE_LOGON bool NOT NULL,
	CREATED timestamp with time zone NOT NULL DEFAULT now(),
	UPDATED timestamp with time zone NOT NULL DEFAULT now()
);
CREATE UNIQUE INDEX UC_EMAIL ON USERS (lower(EMAIL_ADDRESS));
CREATE UNIQUE INDEX UC_USER ON USERS (lower(USER_NAME));
CREATE TRIGGER updated_users BEFORE UPDATE ON USERS FOR EACH ROW EXECUTE PROCEDURE  f_updated_column();

CREATE TABLE ACTIVATE_UUIDS(
	UUID_PK serial NOT NULL PRIMARY KEY,
	USER_FK uuid NOT NULL REFERENCES USERS ON DELETE CASCADE,
	UUID varchar(32) NOT NULL UNIQUE,
	"TYPE" int NOT NULL,
	VALID_UNTIL timestamp with time zone NOT NULL,
	UNIQUE("TYPE", USER_FK)
);

CREATE TABLE DEPARTMENTS (
	DEPARTMENT_PK uuid NOT NULL DEFAULT uuid_generate_v4() PRIMARY KEY,
	GROUP_FK uuid NOT NULL REFERENCES GROUPS,
	NAME varchar(256) NOT NULL,
	CREATED timestamp with time zone NOT NULL DEFAULT now(),
	UPDATED timestamp with time zone NOT NULL DEFAULT now()
);
CREATE UNIQUE INDEX UC_DEPARTMENT_NAME ON DEPARTMENTS (GROUP_FK, lower(NAME));
CREATE TRIGGER updated_departments BEFORE UPDATE ON DEPARTMENTS FOR EACH ROW EXECUTE PROCEDURE  f_updated_column();
CREATE TRIGGER t_change_department
	AFTER INSERT OR UPDATE OR DELETE ON DEPARTMENTS
	FOR EACH ROW EXECUTE PROCEDURE if_modified_func();

